#include "libft.h"

void	ft_swap(void **a, void **b)
{
	if (!a || !b || *a == *b)
		return ;
	*(size_t*)a ^= *(size_t*)b;
	*(size_t*)b ^= *(size_t*)a;
	*(size_t*)a ^= *(size_t*)b;
}
